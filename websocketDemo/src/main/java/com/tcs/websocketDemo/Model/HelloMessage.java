package com.tcs.websocketDemo.Model;

public class HelloMessage {

	String name;

	public HelloMessage() {
		super();
	}

	public HelloMessage(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
